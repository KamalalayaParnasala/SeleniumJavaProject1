package seleniumbasics;

public class ProgramIfCondition {

	public static void main(String[] args) {
		
		int i = 10;
		String s = "Kamala Laya";
		int age= 20;
		boolean answer = true;
		
		if ( i==10) {
			
			System.out.println(i);
		}
		
		if (s =="Kamala Laya") {
			System.out.println(s);
		}
		
		if ( age<=20 ) {
			
			System.out.println(age);
		}
		
		if (answer == true) {
			
			System.out.println(answer);
		}
		
		

	}

}
